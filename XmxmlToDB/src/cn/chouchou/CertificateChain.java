package cn.chouchou;

import java.util.List;

public class CertificateChain {
	private  int num ;
	private  List<String> certs;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public List<String> getCerts() {
		return certs;
	}
	public void setCerts(List<String> certs) {
		this.certs = certs;
	}
	

}
